package com.daksh.lean4j.Login.Constants;

public enum Scopes {

    /**
     * Fully manage your account. This includes projects, users and your organization.
     */
    ADMIN,

    /**
     * Grants read-only access to your projects and your profile information
     */
    READ,

    /**
     * Grants read-write access to your projects, and read-only access for your profile information.
     */
    WRITE
}
